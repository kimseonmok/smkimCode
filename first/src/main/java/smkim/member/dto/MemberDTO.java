package smkim.member.dto;

public class MemberDTO {
	private String mbrNum;
	private String mbrName;
	private String mbrJoinDate;
	private String mbrPwd;
	private String mbrId;
	private String mbrDelChk;
	
	public String getMbrNum() {
		return mbrNum;
	}
	public void setMbrNum(String mbrNum) {
		this.mbrNum = mbrNum;
	}
	public String getMbrName() {
		return mbrName;
	}
	public void setMbrName(String mbrName) {
		this.mbrName = mbrName;
	}
	public String getMbrJoinDate() {
		return mbrJoinDate;
	}
	public void setMbrJoinDate(String mbrJoinDate) {
		this.mbrJoinDate = mbrJoinDate;
	}
	public String getMbrPwd() {
		return mbrPwd;
	}
	public void setMbrPwd(String mbrPwd) {
		this.mbrPwd = mbrPwd;
	}
	public String getMbrId() {
		return mbrId;
	}
	public void setMbrId(String mbrId) {
		this.mbrId = mbrId;
	}
	public String getMbrDelChk() {
		return mbrDelChk;
	}
	public void setMbrDelChk(String mbrDelChk) {
		this.mbrDelChk = mbrDelChk;
	}
	
	

}
